def build_one(rel, D, REPL):
    en_path = os.path.join(EN_ROOT, rel)
    ru_path = os.path.join(RU_ROOT, rel)
    raw = open(en_path, encoding="utf-8").read()
    en_lines = [ln.replace("\r", "") for ln in raw.split("\n")]
    is_index = rel == "index.md"
    out = []
    flags = []
    in_fence = False
    for i, c in enumerate(en_lines, 1):
        st = c.strip()
        is_fence = st.startswith("```")
        if in_fence:
            out.append(c)
            if is_fence:
                in_fence = False
            continue
        if is_fence:
            out.append(c)
            in_fence = True
            continue
        if i in D:
            out.append(D[i])
        elif copy_safe(c):
            out.append(c)
        elif is_index and c.startswith("["):
            out.append(c)  # card line -> copy, REPL applied below
        else:
            flags.append((i, c))
            out.append(c)
    text = "\n".join(out)
    if REPL:
        for a, b in REPL:
            text = text.replace(a, b)
    # parity guard
    assert len(out) == len(en_lines), "PARITY %s: %d != %d" % (
        rel,
        len(out),
        len(en_lines),
    )
    os.makedirs(os.path.dirname(ru_path), exist_ok=True)
    open(ru_path, "wb").write(text.encode("utf-8"))
    return len(en_lines), flags


